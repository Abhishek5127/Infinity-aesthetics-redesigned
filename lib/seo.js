export function absoluteUrl(path = "/") {
  if (path.startsWith("http")) return path;
  return `${SITE_URL}${path.startsWith("/") ? path : `/${path}`}`;
}

export function createMetadata({ title, description, path = "/", image = clinic.image, type = "website" }) {
  const url = absoluteUrl(path);
  const imageUrl = absoluteUrl(image);

  return {
    title,
    description,
    alternates: {
      canonical: url,
    },
    openGraph: {
      title,
      description,
      url,
      siteName: clinic.name,
      images: [{ url: imageUrl, width: 1200, height: 800, alt: `${clinic.name} clinic in Ajmer` }],
      locale: "en_IN",
      type,
    },
    twitter: {
      card: "summary_large_image",
      title,
      description,
      images: [imageUrl],
    },
  };
}

export function clinicSchema(path = "/") {
  return {
    "@context": "https://schema.org",
    "@type": "MedicalClinic",
    "@id": `${SITE_URL}/#clinic`,
    name: clinic.legalName,
    url: absoluteUrl(path),
    image: absoluteUrl(clinic.image),
    telephone: clinic.phone,
    priceRange: clinic.priceRange,
    address: {
      "@type": "PostalAddress",
      streetAddress: clinic.address.street,
      addressLocality: clinic.address.locality,
      addressRegion: clinic.address.region,
      postalCode: clinic.address.postalCode,
      addressCountry: clinic.address.country,
    },
    geo: {
      "@type": "GeoCoordinates",
      latitude: clinic.geo.latitude,
      longitude: clinic.geo.longitude,
    },
    openingHours: clinic.openingHours,
    sameAs: [clinic.instagramUrl, clinic.mapUrl],
    areaServed: areasServed.map((area) => ({
      "@type": "Place",
      name: area,
    })),
    medicalSpecialty: ["Dermatology", "Aesthetic medicine", "Hair restoration"],
  };
}

export function breadcrumbSchema(items) {
  return {
    "@context": "https://schema.org",
    "@type": "BreadcrumbList",
    itemListElement: items.map((item, index) => ({
      "@type": "ListItem",
      position: index + 1,
      name: item.name,
      item: absoluteUrl(item.path),
    })),
  };
}

export function serviceSchema(service, path) {
  const seo = serviceSeo[service.slug] || {};
  return {
    "@context": "https://schema.org",
    "@type": "MedicalProcedure",
    "@id": `${absoluteUrl(path)}#procedure`,
    name: seo.h1 || service.name,
    description: service.overview || service.description,
    procedureType: service.category,
    url: absoluteUrl(path),
    areaServed: areasServed.map((area) => ({
      "@type": "Place",
      name: area,
    })),
    provider: {
      "@id": `${SITE_URL}/#clinic`,
    },
  };
}

export function faqSchema(faqs) {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: faqs.map((faq) => ({
      "@type": "Question",
      name: faq.q,
      acceptedAnswer: {
        "@type": "Answer",
        text: faq.a,
      },
    })),
  };
}
